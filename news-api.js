async function updateViaNewsAPI() {
    console.log('🚀 Starting comprehensive data collection with extended sources...\n');

    const manager = new DataSourceManager();

    // Собираем данные из всех источников
    const result = await manager.collectAll({
        useRSS: true,
        useAPI: true,
        maxEvents: 100,
        minRelevanceScore: 3,
        apiKey: process.env.NEWS_API_KEY
    });

    // Переводим заголовки и описания
    console.log('\n🌐 Translating to Russian...');
    const translatedEvents = result.events.map(event => {
        const translatedTitle = manager.translateText(event.rawTitle || event.title, DICTIONARY);
        const translatedDesc = manager.translateText(event.description, DICTIONARY);

        return {
            ...event,
            title: translatedTitle || event.title,
            description: translatedDesc || event.description,
            rawTitle: undefined,
            relevanceScore: undefined
        };
    });

    // Формируем итоговый output
    const output = {
        metadata: {
            lastUpdated: new Date().toISOString(),
            version: '5.0',
            totalEvents: translatedEvents.length,
            sourcesChecked: result.stats.raw,
            sourcesWorking: result.stats.raw - result.errors.length,
            errors: result.errors,
            updateMethod: 'COMPREHENSIVE_MULTI_SOURCE',
            translationQuality: 'ADVANCED_GRAMMAR',
            language: 'ru',
            stats: result.stats,
            generatedAt: new Date().toLocaleString('ru-RU')
        },
        events: translatedEvents
    };

    // Сохраняем
    const outputPath = path.join(__dirname, 'data', 'events.json');

    if (!fs.existsSync(path.dirname(outputPath))) {
        fs.mkdirSync(path.dirname(outputPath), { recursive: true });
    }

    fs.writeFileSync(outputPath, JSON.stringify(output, null, 2), 'utf8');

    // Выводим статистику
    console.log('\n' + '='.repeat(60));
    console.log('✅ COLLECTION COMPLETE');
    console.log('='.repeat(60));
    console.log(`📊 Total events: ${translatedEvents.length}`);
    console.log(`📡 Raw items processed: ${result.stats.raw}`);
    console.log(`📉 Filtered out: ${result.stats.raw - result.stats.processed}`);
    console.log(`🔧 Errors: ${result.errors.length}`);

    console.log('\n📈 By type:');
    Object.entries(result.stats.byType).forEach(([type, count]) => {
        console.log(`    ${type}: ${count}`);
    });

    console.log('\n🌍 Top countries:');
    Object.entries(result.stats.byCountry)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 5)
        .forEach(([country, count]) => {
            console.log(`    ${country}: ${count}`);
        });

    console.log('\n📝 Sample translated titles:');
    translatedEvents.slice(0, 5).forEach((e, i) => {
        console.log(`    ${i + 1}. [${e.type}] ${e.title.substring(0, 60)}...`);
    });

    return output;
}

